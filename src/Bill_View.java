    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed
        // TODO add your handling code here:
        Connection conn=null;
        ResultSet rs=null;
        PreparedStatement pst=null;
        conn=JavaConnect.ConnectDB();
        try
        {
            switch (select_txt.getSelectedItem().toString()) {
                case "Name":
                {
                    String sql="select * from Registration where Name=?";
                    pst=conn.prepareStatement(sql);
                    pst.setString(1,search_txt.getText());
                    rs=pst.executeQuery();
                    if(rs.next())
                    {
                        String a=rs.getString("Name");
                        name_txt.setText(a);
                        String b=rs.getString("ID");
                        Id_txt.setText(b);
                        String c=rs.getString("Mobile");
                        mobile_txt.setText(c);
                        String d=rs.getString("Profession");
                        Profession_txt.setText(d);
                        String e=rs.getString("Room");
                        romm_txt.setSelectedItem(e);
                        String f=rs.getString("RoomType");
                        type_txt.setSelectedItem(f);
                        String g=rs.getString("CheckIn");
                        ((JTextField)date_1.getDateEditor().getUiComponent()).setText(g);
                        String h=rs.getString("CheckOut");
                        ((JTextField)date_2.getDateEditor().getUiComponent()).setText(h);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Sorry No Result");
                    } break;
                }
                case "ID":
                {
                    String sql="select * from Registration where ID=?";
                    pst=conn.prepareStatement(sql);
                    pst.setString(1,search_txt.getText());
                    rs=pst.executeQuery();
                    if(rs.next())
                    {
                        String a=rs.getString("Name");
                        name_txt.setText(a);
                        String b=rs.getString("ID");
                        Id_txt.setText(b);
                        String c=rs.getString("Mobile");
                        mobile_txt.setText(c);
                        String d=rs.getString("Profession");
                        Profession_txt.setText(d);
                        String e=rs.getString("Room");
                        romm_txt.setSelectedItem(e);
                        String f=rs.getString("RoomType");
                        type_txt.setSelectedItem(f);
                        String g=rs.getString("CheckIn");
                        ((JTextField)date_1.getDateEditor().getUiComponent()).setText(g);
                        String h=rs.getString("CheckOut");
                        ((JTextField)date_2.getDateEditor().getUiComponent()).setText(h);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Sorry No Result");
                    } break;
                }
                default:
                JOptionPane.showMessageDialog(null, "Please Click on Select Field !");
                break;
            }
        }
        catch(HeadlessException | SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Please Click on Select Field !");
        }
        finally
        {
            try
            {
                if(rs!=null)
                {
                    rs.close();

                }
                if(conn!=null)
                {
                    conn.close();
                }
                if(pst!=null)
                {
                    pst.close();
                }
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null, "Connection not has been closed yet");
            }
        }
    }//GEN-LAST:event_search_btnActionPerformed

