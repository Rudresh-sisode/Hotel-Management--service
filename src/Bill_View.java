    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        // TODO add your handling code here:
        Connection conn=null;
        ResultSet rs=null;
        PreparedStatement pst=null;
        conn=JavaConnect.ConnectDB();
        String aa=name_txt.getText();
        String bb=Id_txt.getText();
        if(name_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Enter Customer Name !");
        }
        else if(Id_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Enter ID(Must not Unique) !");
        }
        else if(mobile_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Enter Mobile Number !");
        }
        else if(Profession_txt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Enter Profession !");
        }
        else if(romm_txt.getSelectedItem().toString().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Select Room !");
        }
        else if(type_txt.getSelectedItem().toString().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Select Room Type!");
        }
        else if(((JTextField)date_1.getDateEditor().getUiComponent()).getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Choose Date !");
        }
        else if(((JTextField)date_2.getDateEditor().getUiComponent()).getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Choose Date !");
        }
        else
        {
            try
            {
                String sql="insert into Customer_Bill values(?,?,?,?,?,?,?,?,?)";
                pst=conn.prepareStatement(sql);
                pst.setString(1, name_txt.getText());
                pst.setString(2, Id_txt.getText());
                pst.setString(3, mobile_txt.getText());
                pst.setString(4, Profession_txt.getText());
                pst.setString(5, romm_txt.getSelectedItem().toString());
                pst.setString(6, type_txt.getSelectedItem().toString());
                pst.setString(7, ((JTextField)date_1.getDateEditor().getUiComponent()).getText());
                pst.setString(8, ((JTextField)date_2.getDateEditor().getUiComponent()).getText());
                pst.setString(9, total.getText());
                int p=JOptionPane.showConfirmDialog(null,"Do you want to save","Conform Save",JOptionPane.YES_NO_OPTION);
                if(p==0)
                {
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Saved");

                 
                   
                }
            }
            catch(HeadlessException | SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "sorry Error Occured\n"+ex);
            }
            finally
            {
                try
                {
                    if(rs!=null)
                    {
                        rs.close();

                    }
                    if(conn!=null)
                    {
                        conn.close();
                    }
                    if(pst!=null)
                    {
                        pst.close();
                    }
                }
                catch(SQLException e)
                {
                    JOptionPane.showMessageDialog(null, "Connection not has been closed yet");
                }
            }
        }

    }//GEN-LAST:event_save_btnActionPerformed

